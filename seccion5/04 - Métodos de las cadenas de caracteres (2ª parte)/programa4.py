# Demostración del método the lstrip():
print("[" + " tau ".lstrip() + "]")

print("www.cisco.com".lstrip("w."))

# Demostración del método rstrip():
print("[" + " upsilon ".rstrip() + "]")
print("cisco.com".rstrip(".com"))

# Demostración del método strip():
print("[" + "   aleph   ".strip() + "]")
print("*****aleph**".strip("*"))
